class BalancoTrimestral{
    public static void main(String[] args) {
        int gastosJaneiro;
        gastosJaneiro = 15000;
        int gastosFevereiro;
        gastosFevereiro = 23000;
        int gastosMarco;
        gastosMarco = 17000;
        int gastosTrimestre = gastosFevereiro + gastosJaneiro + gastosMarco;
        System.out.println(gastosTrimestre);
    }

}